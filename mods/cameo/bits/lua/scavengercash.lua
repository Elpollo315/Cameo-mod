
p1scavengercash = function()
	if p1 and p1.IsBot then
		supplytruckarmed = p1.GetActorsByType("2100supplytruckarmed")
		for _,Transfer in pairs(supplytruckarmed) do
			hq = p1.GetActorsByType("2100hq.scavenger")
			chopshop = p1.GetActorsByType("2100chopshop")
			chopshopadv = p1.GetActorsByType("2100chopshopadv")
			scavhangar = p1.GetActorsByType("2100scavhangar")
			for _,hq in pairs(hq) do Transfer.DeliverCash(hq) end
			for _,chopshop in pairs(chopshop) do Transfer.DeliverCash(chopshop) end
			for _,chopshopadv in pairs(chopshopadv) do Transfer.DeliverCash(chopshopadv) end
			for _,scavhangar in pairs(scavhangar) do Transfer.DeliverCash(scavhangar) end
		end
	end
	Trigger.AfterDelay(DateTime.Seconds(Utils.RandomInteger(1, 10)), function()
		p1scavengercash()
	end)
end
